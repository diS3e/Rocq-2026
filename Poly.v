    типа [Z]. Строго говоря, если расставить все скобки, этот тип на самом деле
    выглядит как [X -> (Y -> Z)].  То есть, если у нас есть [f : X -> Y -> Z], и
    мы передаём [f] вход типа [X], она вернёт нам функцию типа [Y -> Z]. Если мы
    затем уже этой функции передадим на вход что-либо, имеющее тип [Y], она
    вернёт нам результат типа [Z].  Таким образом, все функции в Rocq принимают
    на вход только один аргумент, просто некоторые из возвращают функцию
    в качестве результата.  В точности благодаря этому механизму в Rocq есть
    частичное применение, с которым мы познакомились выше, в [plus3].

    В свою очередь, функции типа [X * Y -> Z] (с полной расстановкой скобок этот
    тип пишется как [(X * Y) -> Z]) требуют своим единственным входом пару (типа
    [X * Y]).  Оба аргумента должны быть переданы одновременно; для функции
    с таким типом возможностей для частичного применения нет.

    Однако заметим, что функции этих двух типов можно конвертировать
    между собой. Так, конвертация из [X * Y -> Z] в [X -> Y -> Z] называется
    _каррирование_, в честь логика Хаскелла Карри.  Конвертация в обратную
    сторону, из [X -> Y -> Z] в [X * Y -> Z], называется _раскаррирование_
    (uncurrying).  *)

(** Мы можем определить каррирование следующим образом: *)

