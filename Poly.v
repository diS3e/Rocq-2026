(** *** Вывод аннотаций типов *)

(** Вновь выпишем определение [repeat], но в этот раз не будем указывать типы
    аргументов.  Примет ли Rocq такое определение? *)

Fixpoint repeat' X x count : list X :=
  match count with
  | 0        => nil X
  | S count' => cons X x (repeat' X x count')
  end.

(** Ну конечно.  Посмотрим, какой же тип он выдал [repeat']... *)

Check repeat'
  : forall X : Type, X -> nat -> list X.
Check repeat
  : forall X : Type, X -> nat -> list X.

(** У него в точности такой же тип, как и у [repeat].  Rocq воспользовался
    _выводом типов_, чтобы выяснить, какие типы должны быть у [X], [x] и [count]
    на основе того, как они используются.  Например, раз [X] используется как
    аргумент [cons], он должен быть [Типом], ведь [cons] своим первым аргументом
    ожидает [Type]; сопоставление [count] с [0] и [S] значит, что он должен
    иметь тип [nat]; и так далее.

    Это мощное свойство системы значит, что нам не придётся всегда и везде
    выписывать все аннотации типов; однако, явные аннотации типов всё ещё могут
    быть полезны для документации и проверок на осмысленность, так что мы всё же
    продолжим их использовать бОльшую часть времени. *)

(* ----------------------------------------------------------------- *)
(** *** Синтез аргументов-типов *)

(** Чтобы использовать полиморфную функцию, нам нужно передать ей
    какое-то количество типов помимо её остальных аргументов. Например,
    рекурсивный вызов в теле функции [repeat] должен передать тип [X].
    Но раз второй аргумент функции [repeat] обязан быть элементом [X],
    выглядит совершенно очевидным, что первый аргумент может быть равен
    только [X] -- зачем же нам тогда выписывать его явно?

    К счастью, Rocq позволяет нам избежать такого рода повторов. Вместо любого
    типового аргумента мы можем написать "дырку" [_], которую можно прочитать
    как "Пожалуйста, постарайся выяснить сам, что сюда нужно подставить".
    Точнее говоря, когда Rocq встречает [_], он будет пытаться _унифицировать_
    всю локально доступную информацию -- тип применяемой функции, типы других
    аргументов, а также тип, ожидаемый контекстом, в котором появляется
    применение -- чтобы выяснить, какой конкретный тип должен стоять
    на месте [_].

    Звучит похоже на вывод аннотаций типов -- и, действительно, обе процедуры
    полагаются на одни и те же базовые механизмы. Вместо того, чтобы просто
    опустить типы некоторых аргументов функции, вроде

      repeat' X x count : list X :=

    мы точно так же можем заменить типы дырками

      repeat' (X : _) (x : _) (count : _) : list X :=

    чтобы попросить Rocq попытаться вывести недостающую информацию.

    Используя дырки, функцию [repeat] можно записать вот так: *)

Fixpoint repeat'' X x count : list X :=
  match count with
  | 0        => nil _
  | S count' => cons _ x (repeat'' _ x count')
  end.

(** В данном случае мы особо не сэкономили, записав [_] вместо [X]. Однако
    во многих случаях разница как по объёму кода, так и по читаемости
    существенная. Например, пусть мы хотим выписать список, содержащий числа
    [1], [2], [3]. Вместо такого... *)

Definition list123 :=
  cons nat 1 (cons nat 2 (cons nat 3 (nil nat))).

(** ...можно использовать дырки и записать: *)

Definition list123' :=
  cons _ 1 (cons _ 2 (cons _ 3 (nil _))).

(* ----------------------------------------------------------------- *)
(** *** Неявные аргументы *)

(** На самом деле, мы можем пойти ещё дальше и вообще не писать [_] в
    большинстве случаев, попросив Rocq _всегда_ выводить типовые аргументы
    некоторой функции.

    Директива [Arguments] сообщает имя функции (либо конструктора) и далее
    перечисляет имена аргументов (начиная с первых), которые нужно считать
    неявными; каждое такое имя окружено фигурными скобками. *)

Arguments nil {X}.
Arguments cons {X}.
Arguments repeat {X}.

(** Теперь в данном примере нам вообще не нужно указывать типовые аргументы: *)

Definition list123'' := cons 1 (cons 2 (cons 3 nil)).

(** Также мы можем объявить аргумент неявным ещё на этапе объявления функции,
    окружив его в фигурные скобки вместо круглых. Например: *)

Fixpoint repeat''' {X : Type} (x : X) (count : nat) : list X :=
  match count with
  | 0        => nil
  | S count' => cons x (repeat''' x count')
  end.

(** (Обратите внимание, что нам даже не нужно было предоставлять типовой
    аргумент в рекурсивном вызове [repeat'''].  Действительно, было бы
    некорректно его предоставлять, поскольку Rocq его и не ожидает.) *)

(** Мы будем использовать такой стиль везде, где это возможно, но мы продолжим
    использовать явные объявления [Argument] для конструкторов [Индуктивных]
    типов данных.  Причина заключается в том, что, объявив параметр индуктивного
    типа неявным, мы сделаем параметр неявным не только для конструкторов, но и
    для самого объявления типа.  Например, рассмотрим следующее альтернативное
    определение [list]: *)

Inductive list' {X:Type} : Type :=
  | nil'
  | cons' (x : X) (l : list').

(** Поскольку [X] объявлен неявным для _всего_ определения индуктивного типа,
    включая сам [list'], мы теперь вынуждены писать просто [list'] вне
    зависимости от того, о каких списках мы говорим, вместо того чтобы
    писать [list' nat], [list' bool] или что-то ещё; это уже перебор. *)

(** Давайте завершим, переписав некоторые стандартные функции на
    полиморфные списки... *)

Fixpoint app {X : Type} (l1 l2 : list X) : list X :=
  match l1 with
  | nil      => l2
  | cons h t => cons h (app t l2)
  end.

Fixpoint rev {X:Type} (l:list X) : list X :=
  match l with
  | nil      => nil
  | cons h t => app (rev t) (cons h nil)
  end.

Fixpoint length {X : Type} (l : list X) : nat :=
  match l with
  | nil => 0
  | cons _ l' => S (length l')
  end.

Example test_rev1 :
  rev (cons 1 (cons 2 nil)) = (cons 2 (cons 1 nil)).
Proof. reflexivity. Qed.

Example test_rev2:
  rev (cons true nil) = cons true nil.
Proof. reflexivity. Qed.

Example test_length1: length (cons 1 (cons 2 (cons 3 nil))) = 3.
Proof. reflexivity. Qed.

(* ----------------------------------------------------------------- *)
(** *** Явная передача аргументов-типов *)

(** Всё же есть одна небольшая проблема с объявлением аргументов неявными;
    она состоит в том, что иногда Rocq всё же недостаточно локальной информации,
    чтобы определить аргумент-тип; в таких случаях нужно сообщить Rocq, что прям
    вот в этом случае мы хотем передать аргумент явно. Вот пример: *)

Fail Definition mynil := nil.

(** (Квалификатор [Fail], который использован здесь перед [Definition],
    можно использовать вместе с _любой_ командой. Его используют, чтобы
    убедиться, что исполнение команды действительно завершается ошибкой.
    В случае, если это действительно так, Rocq печатает соответствующее
    сообщение об ошибке, но продолжает дальнейшую обработку файла.)

    Здесь Rocq возвращает ошибку потому, что он не знает, какой типовой аргумент
    нужно подставить в [nil]. Мы можем помочь ему, предъявив явную аннотацию
    типа (так что у Rocq станет доступно больше информации, когда он доберётся
    до "применения функции" [nil]): *)

Definition mynil : list nat := nil.

(** Также можно сделать все аргументы явными,
    добавив [@] перед именем функции. *)

Check @nil : forall X : Type, list X.

Definition mynil' := @nil nat.

(** (Также мы не можем и написать здесь [nil nat], без [@]: объявление
    [Implicit Arguments] выше значит, что [nil nat] теперь понимается Rocq как
    [@nil ?X nat], где [?X] -- некоторый неизвестный тип, который должен быть
    подставлен выводом неявных аргументов.) *)
Fail Check (nil nat).

(** Используя вывод аргументов и неявные аргументы, мы можем определить
    удобную нотацию для списков, как раньше.  Так как мы сделали аргументы-типы
    конструкторов неявными, Rocq будет автоматически их выводить, когда мы будем
    эти нотации использовать. *)

Notation "x :: y" := (cons x y)
                     (at level 60, right associativity).
Notation "[ ]" := nil.
Notation "[ x ; .. ; y ]" := (cons x .. (cons y []) ..).
Notation "x ++ y" := (app x y)
                     (at level 60, right associativity).

(** Теперь списки можно записывать ровно так, как хотелось бы: *)

Definition list123''' := [1; 2; 3].

