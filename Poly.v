(** То, что здесь происходит, называется *частичным применением*. В Rocq,
    конструктор типов [->] правоассоциативен, т.е. тип функции вроде
    [A -> B -> C] воспринимается как [A -> (B -> C)], т.е. "функция из А
    в функции из B в C".

