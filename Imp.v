(** * Арифметические и логические выражения *)

(** Мы представим Imp в трёх частях: начнём с языка _арифметических и логических
    выражений_, затем расширим его _переменными_, и, наконец, введём язык
    _команд_, включающий присваивания, условные ветвления, последовательное
    исполнение и циклы. *)

(* ================================================================= *)
(** ** Синтаксис *)

Module AExp.

(** Эти два определения задают _абстрактный синтаксис_ (AST)
    арифметических и логических выражений. *)

Inductive aexp : Type :=
  | ANum (n : nat)
  | APlus (a1 a2 : aexp)
  | AMinus (a1 a2 : aexp)
  | AMult (a1 a2 : aexp).

Inductive bexp : Type :=
  | BTrue
  | BFalse
  | BEq (a1 a2 : aexp)
  | BNeq (a1 a2 : aexp)
  | BLe (a1 a2 : aexp)
  | BGt (a1 a2 : aexp)
  | BNot (b : bexp)
  | BAnd (b1 b2 : bexp).

(** В данной главе мы по большей части опустим перевод из конкретного
    синтаксиса (на котором пишет гипотетический программист на Imp)
    в абстрактный. Этот процесс называется "парсинг" либо "синтаксический
    анализ"; он, например, преобразует строчку вида ["1 + 2 * 3"] в AST

      APlus (ANum 1) (AMult (ANum 2) (ANum 3)).

    Дополнительная глава [ImpParser] содержит простой лексический и
    синтаксический анализаторы, которые производят этот перевод.  Вам не нужно
    читать ту главу, чтобы понять эту, но если Вы ещё не проходили курс,
    в котором разбираются эти техники (например, курс по компиляторам), Вам
    может быть полезно её проглядеть. *)

(** Для сравнения, вот грамматика в BNF (форма Бэкуса-Наура), задающая тот же
    абстрактный синтаксис:

    a := nat
        | a + a
        | a - a
        | a * a

    b := true
        | false
        | a = a
        | a <> a
        | a <= a
        | a > a
        | ~ b
        | b && b
*)

(** В сравнении с версией на Rocq...

       - BNF менее формальна -- например, она больше похожа на демонстрируемый
         пользователю синтаксис выражений (как тот факт, что операция сложения
         записывается инфиксно с помощью оператора [+]), но в то же время
         опускает другие аспекты лексического и синтаксического анализа
         (например, приоритеты [+], [-] и [*], использование скобок
         для группировки подвыражений и т.д.).  Для превращения этого описания
         в формальное определение (например, пригодное для реализации
         компилятора) требуется как некоторая дополнительная информация, так и
         человеческий разум.

         Версия на Rocq опускает всю информацию о текстовом представлении
         и концентрируется только на абстрактном синтаксисе.

       - С другой стороны, версия в BNF более легковесна и более читаема.
         Неформальность делает её гибкой, что является большим преимуществом
         в обсуждениях "у доски", где передача основной идеи важнее уточнения
         вороха мелких деталей.

         И действительно, есть множество "BNF-подобных" нотаций, которыми
         пользуются специалисты, не уточняя, какой из них они пользуются, ведь
         это и не нужно: на данном этапе важно только общее, неформальное
         понимание происходящего.

    Лучше всего уметь работать с обеими вариантами нотации: с неформальной --
    для коммуникации между людьми; с формальной -- для реализации языков
    и доказательств их свойств. *)

(* ================================================================= *)
(** ** Вычисление *)

(** _Вычисление_ арифметического выражения в результате получает число. *)

Fixpoint aeval (a : aexp) : nat :=
  match a with
  | ANum n => n
  | APlus  a1 a2 => (aeval a1) + (aeval a2)
  | AMinus a1 a2 => (aeval a1) - (aeval a2)
  | AMult  a1 a2 => (aeval a1) * (aeval a2)
  end.

Example test_aeval1:
  aeval (APlus (ANum 2) (ANum 2)) = 4.
Proof. reflexivity. Qed.

(** Аналогично, логические выражения вычисляются в значение типа [bool]. *)

Fixpoint beval (b : bexp) : bool :=
  match b with
  | BTrue       => true
  | BFalse      => false
  | BEq a1 a2   => (aeval a1) =? (aeval a2)
  | BNeq a1 a2  => negb ((aeval a1) =? (aeval a2))
  | BLe a1 a2   => (aeval a1) <=? (aeval a2)
  | BGt a1 a2   => negb ((aeval a1) <=? (aeval a2))
  | BNot b1     => negb (beval b1)
  | BAnd b1 b2  => andb (beval b1) (beval b2)
  end.

(* ================================================================= *)
(** ** Оптимизация *)

(** Мы дали не очень-то много определений, но мы уже можем извлечь из них
    кое-какую пользу.  Допустим, мы определили функцию, которая получает
    арифметическое выражение, а затем немного его упрощает, заменяя каждое
    вхождение [0 + e] (т.е. [APlus (ANum 0) e]) на просто [e]. *)

Fixpoint optimize_0plus (a:aexp) : aexp :=
  match a with
  | ANum n => ANum n
  | APlus (ANum 0) e2 => optimize_0plus e2
  | APlus  e1 e2 => APlus  (optimize_0plus e1) (optimize_0plus e2)
  | AMinus e1 e2 => AMinus (optimize_0plus e1) (optimize_0plus e2)
  | AMult  e1 e2 => AMult  (optimize_0plus e1) (optimize_0plus e2)
  end.

(** Чтобы получить немного уверенности в том, что наша оптимизация
    работает правильно, мы можем протестировать её на нескольких примерах
    и проверить, всё ли хорошо с результатом. *)

Example test_optimize_0plus:
  optimize_0plus (APlus (ANum 2)
                        (APlus (ANum 0)
                               (APlus (ANum 0) (ANum 1))))
  = APlus (ANum 2) (ANum 1).
Proof. reflexivity. Qed.

(** Но если мы хотим _убедиться_ в корректности оптимизации -- т.е.
    что вычисление оптимизированного выражения _всегда_ возвращает тот же
    результат, что и вычисление исходного выражения -- мы можем доказать это! *)

Theorem optimize_0plus_sound: forall a,
  aeval (optimize_0plus a) = aeval a.
Proof.
  intros a. induction a.
  - (* ANum *) reflexivity.
  - (* APlus *) destruct a1 eqn:Ea1.
    + (* a1 = ANum n *) destruct n eqn:En.
      * (* n = 0 *)  simpl. apply IHa2.
      * (* n <> 0 *) simpl. rewrite IHa2. reflexivity.
    + (* a1 = APlus a1_1 a1_2 *)
      simpl. simpl in IHa1. rewrite IHa1.
      rewrite IHa2. reflexivity.
    + (* a1 = AMinus a1_1 a1_2 *)
      simpl. simpl in IHa1. rewrite IHa1.
      rewrite IHa2. reflexivity.
    + (* a1 = AMult a1_1 a1_2 *)
      simpl. simpl in IHa1. rewrite IHa1.
      rewrite IHa2. reflexivity.
  - (* AMinus *)
    simpl. rewrite IHa1. rewrite IHa2. reflexivity.
  - (* AMult *)
    simpl. rewrite IHa1. rewrite IHa2. reflexivity.  Qed.

