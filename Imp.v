     - Функции вычисляются ядром Rocq, так что в ходе доказательств выражения
       упрощаются автоматически.

    Более того, функции можно напрямую "извлекать" из Gallina в исполняемый код
    на OCaml или Haskell.

    В конечном итоге, выбор чаще всего сводится либо к специфике конкретной
    ситуации, либо просто остаётся делом вкуса.  В больших проектах на Rocq
    часто встречаются определения, данные в _обоих_ стилях, плюс доказывается
    лемма об их эквивалентности, что позволяет последующим доказательствам
    переключаться между стилями на лету. *)

