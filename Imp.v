(** Далее нам нужно определить, что значит исполнить команду на языке Imp.
    Тот факт, что циклы [while] не обязательно завершаются, делает определение
    функции вычисления непростым делом... *)

(* ================================================================= *)
(** ** Исполнение как функция (провал) *)

(** Вот попытка определить функцию исполнения команд
    (с бредовым случаем для [while]). *)

Fixpoint ceval_fun_no_while (st : state) (c : com) : state :=
  match c with
    | <{ skip }> =>
        st
    | <{ x := a }> =>
        (x !-> aeval st a ; st)
    | <{ c1 ; c2 }> =>
        let st' := ceval_fun_no_while st c1 in
        ceval_fun_no_while st' c2
    | <{ if b then c1 else c2 end}> =>
        if (beval st b)
          then ceval_fun_no_while st c1
          else ceval_fun_no_while st c2
    | <{ while b do c end }> =>
        st  (* бредово *)
  end.

(** В более конвенциональном функциональном языке программирования вроде OCaml
    или Haskell мы могли бы обработать случай [while] вот так:

        Fixpoint ceval_fun (st : state) (c : com) : state :=
          match c with
            ...
            | <{ while b do c end}> =>
                if (beval st b)
                  then ceval_fun st <{c ; while b do c end}>
                  else st
          end.

    Rocq не примет такое определение ("Error: Cannot guess decreasing argument
    of fix"), поскольку функция, которую мы хотим определить, не обязательно
    завершается. И действительно: такая версия [ceval_fun], будучи применённой
    к команде [loop], не завершится никогда. Поскольку Rocq стремится быть
    не только функциональным языком программирования, но и непротиворечивой
    логикой, любые потенциально не завершающиеся функции им отвергаются.

    Вот пример, показывающий, что может пойти не так, если бы Rocq разрешал
    не завершающиеся рекурсивные функции:

         Fixpoint loop_false (n : nat) : False := loop_false n.

    То есть, утверждения вроде [False] стали бы доказуемыми ([loop_false 0] было
    бы доказательством [False]), что на корню уничтожило бы логическую
    непротиворечивость Rocq.

    Таким образом, поскольку она завершается не на всех входах, [ceval_fun]
    в Rocq записать нельзя -- по крайней мере, без дополнительных трюков
    и обходных путей. *)

(* ================================================================= *)
(** ** Исполнение как отношение *)

(** Вот способ получше: определить [ceval] как _отношение_, а не как _функцию_
    -- т.е., сделать её результатом [Prop] вместо [state], как мы уже делали
    с [aevalR] выше. *)

(** Это важный переход.  Он не только освобождает нас от неуклюжих трюков, но и
    даёт гораздо больше гибкости в определении.  Например, если мы добавим
    недетерминированные средства вроде [any] в язык, мы тогда хотим, чтобы и
    определение вычисления было недетерминированным -- т.е. оно будет не только
    не тотальным, но и даже не функцией! *)

(** Для отношения [ceval] мы будем использовать нотацию [st =[ c ]=> st']: она
    значит, что исполнение программы [c] из начального состояния [st] приводит
    в конечное состояние [st'].  Можно читать как
    "[c] отправляет состояние [st] в [st']". *)

(* ----------------------------------------------------------------- *)
(** *** Операционная семантика *)

(** Вот "бумажное" определение вычисления, представленное в виде правил вывода:

                           -----------------                            (E_Skip)
                           st =[ skip ]=> st

                           aeval st a = n
                   -------------------------------                      (E_Asgn)
                   st =[ x := a ]=> (x !-> n ; st)

                           st  =[ c1 ]=> st'
                           st' =[ c2 ]=> st''
                         ---------------------                           (E_Seq)
                         st =[ c1;c2 ]=> st''

                          beval st b = true
                           st =[ c1 ]=> st'
                --------------------------------------               (E_IfTrue)
                st =[ if b then c1 else c2 end ]=> st'

                         beval st b = false
                           st =[ c2 ]=> st'
                --------------------------------------              (E_IfFalse)
                st =[ if b then c1 else c2 end ]=> st'

                         beval st b = false
                    -----------------------------                 (E_WhileFalse)
                    st =[ while b do c end ]=> st

                          beval st b = true
                           st =[ c ]=> st'
                  st' =[ while b do c end ]=> st''
                  --------------------------------                 (E_WhileTrue)
                  st  =[ while b do c end ]=> st''
*)

(** Ниже дано формальное определение.  Убедитесь, что Вы понимаете, как
    оно соотносится с правилами вывода. *)

Reserved Notation
         "st0 '=[' c ']=>' st1"
         (at level 40, c custom com at level 99,
          st0 constr, st1 constr at next level,
          format "'[hv' st0  =[ '/  ' '[' c ']' '/' ]=>  st1 ']'").

Inductive ceval : com -> state -> state -> Prop :=
  | E_Skip : forall st,
      st =[ skip ]=> st
  | E_Asgn  : forall st a n x,
      aeval st a = n ->
      st =[ x := a ]=> (x !-> n ; st)
  | E_Seq : forall c1 c2 st st' st'',
      st  =[ c1 ]=> st'  ->
      st' =[ c2 ]=> st'' ->
      st  =[ c1 ; c2 ]=> st''
  | E_IfTrue : forall st st' b c1 c2,
      beval st b = true ->
      st =[ c1 ]=> st' ->
      st =[ if b then c1 else c2 end]=> st'
  | E_IfFalse : forall st st' b c1 c2,
      beval st b = false ->
      st =[ c2 ]=> st' ->
      st =[ if b then c1 else c2 end]=> st'
  | E_WhileFalse : forall b st c,
      beval st b = false ->
      st =[ while b do c end ]=> st
  | E_WhileTrue : forall st st' st'' b c,
      beval st b = true ->
      st  =[ c ]=> st' ->
      st' =[ while b do c end ]=> st'' ->
      st  =[ while b do c end ]=> st''

  where "st0 =[ c ]=> st1" := (ceval c st0 st1).

