(** * Выражения с переменными *)

(** Наконец вернёмся к определению Imp. Следующий пункт нашей программы --
    обогатить арифметические и логические выражения переменными.

    Для упрощения положим, что все переменные глобальные, и в них сохраняются
    только числа. *)

(* ================================================================= *)
(** ** Состояния *)

(** Поскольку мы хотим хранить и запрашивать текущие значения переменных, мы
    будем использовать тотальные словари из главы [Maps].

    _Состояние машины_ (либо просто _состояние_) представляет текущие значения
    всех переменных на некоторый момент исполнения программы. *)

(** Для простоты будем считать, что состояние определено для _всех_
    переменных, хотя любая программа может использовать только конечное их
    количество.  Поскольку каждая переменная хранит натуральное число, мы можем
    представить состояние тотальным словарём из строчек (имён переменных)
    в [nat], а [0] будет нашим значением по умолчанию. *)

Definition state := total_map nat.

(* ================================================================= *)
(** ** Синтаксис *)

(** Мы можем добавить переменные в арифметические выражения, которые мы
    определили ранее, включив ещё один конструктор: *)

Inductive aexp : Type :=
  | ANum (n : nat)
  | AId (x : string)              (* <--- NEW *)
  | APlus (a1 a2 : aexp)
  | AMinus (a1 a2 : aexp)
  | AMult (a1 a2 : aexp).

(** Определение некоторых переменных сделает наши примеры более читаемыми: *)

Definition W : string := "W".
Definition X : string := "X".
Definition Y : string := "Y".
Definition Z : string := "Z".

(** (Договорённость по использованию заглавных для именования переменных
    ([X], [Y], [Z]) слегка конфликтует с нашим более ранним использованием
    заглавных для типов.  Поскольку мы особо не пользуемся полиморфизмом
    в главах, посвящённых Imp, эта коллизия не должна приводить к путанице.) *)

(** Определение [bexp]ов не меняется (разве что теперь мы ссылаемся на новое
    определение [aexp]): *)

Inductive bexp : Type :=
  | BTrue
  | BFalse
  | BEq (a1 a2 : aexp)
  | BNeq (a1 a2 : aexp)
  | BLe (a1 a2 : aexp)
  | BGt (a1 a2 : aexp)
  | BNot (b : bexp)
  | BAnd (b1 b2 : bexp).

(* ================================================================= *)
(** ** Нотации *)

(** Чтобы сделать программы на Imp проще для чтения и написания, мы введём
    несколько нотаций и неявных преобразований.  *)

(** Вам не нужно в точности понимать, что делают эти объявления.

    Однако, кратко:
       - Объявление [Coercion] заявляет, что функция (либо конструктор) может
         быть использована системой типов неявно, чтобы преобразовать значение
         одного типа в другой.  Например, объявление неявного преобразования
         для [AId] позволяет нам использовать строчки там, где ожидается [aexp];
         строчка неявно будет обёрнута в вызов [AId].
       - [Declare Custom Entry com] указывает Rocq создать новую грамматику
         для парсинга выражений и программ на Imp. Первое объявление нотации,
         следующее за ним, говорит Rocq, что всё между [<{] и [}>] должно
         распознаваться с помощью грамматики Imp. Опять же, понимать детали
         необязательно, но важно понимать, что мы задаём _новые_ интерпретации
         для некоторых знакомых операторов вроде [+], [-], [*], [=], [<=] и т.д.
         внутри [<{] и [}>]. *)

Coercion AId : string >-> aexp.
Coercion ANum : nat >-> aexp.

Declare Custom Entry com.
Declare Scope com_scope.

Notation "<{ e }>" := e
  (e custom com, format "'[hv' <{ '/  ' '[v' e ']' '/' }> ']'") : com_scope.

Notation "( x )" := x (in custom com, x at level 99).
Notation "x" := x (in custom com at level 0, x constr at level 0).

Notation "f x .. y" := (.. (f x) .. y)
                  (in custom com at level 0, only parsing,
                  f constr at level 0, x constr at level 1,
                      y constr at level 1).
Notation "x + y"   := (APlus x y) (in custom com at level 50, left associativity).
Notation "x - y"   := (AMinus x y) (in custom com at level 50, left associativity).
Notation "x * y"   := (AMult x y) (in custom com at level 40, left associativity).
Notation "'true'"  := true (at level 1).
Notation "'true'"  := BTrue (in custom com at level 0).
Notation "'false'" := false (at level 1).
Notation "'false'" := BFalse (in custom com at level 0).
Notation "x <= y"  := (BLe x y) (in custom com at level 70, no associativity).
Notation "x > y"   := (BGt x y) (in custom com at level 70, no associativity).
Notation "x = y"   := (BEq x y) (in custom com at level 70, no associativity).
Notation "x <> y"  := (BNeq x y) (in custom com at level 70, no associativity).
Notation "x && y"  := (BAnd x y) (in custom com at level 80, left associativity).
Notation "'~' b"   := (BNot b) (in custom com at level 75, right associativity).

Open Scope com_scope.

(** Теперь мы можем писать [3 + (X * 2)] вместо [APlus 3 (AMult X 2)],
    и [true && ~(X <= 4)] вместо [BAnd true (BNot (BLe X 4))]. *)

Definition example_aexp : aexp := <{ 3 + (X * 2) }>.
Definition example_bexp : bexp := <{ true && ~(X <= 4) }>.

(* ================================================================= *)
(** ** Вычисление *)

(** Теперь нам нужно расширить вычислители, чтобы они очевидным образом
    обрабатывали переменные, принимая состояние [st] отдельным аргументом: *)

Fixpoint aeval (st : state) (* <--- NEW *)
               (a : aexp) : nat :=
  match a with
  | ANum n => n
  | AId x => st x                                (* <--- NEW *)
  | <{a1 + a2}> => (aeval st a1) + (aeval st a2)
  | <{a1 - a2}> => (aeval st a1) - (aeval st a2)
  | <{a1 * a2}> => (aeval st a1) * (aeval st a2)
  end.

Fixpoint beval (st : state) (* <--- NEW *)
               (b : bexp) : bool :=
  match b with
  | <{true}>      => true
  | <{false}>     => false
  | <{a1 = a2}>   => (aeval st a1) =? (aeval st a2)
  | <{a1 <> a2}>  => negb ((aeval st a1) =? (aeval st a2))
  | <{a1 <= a2}>  => (aeval st a1) <=? (aeval st a2)
  | <{a1 > a2}>   => negb ((aeval st a1) <=? (aeval st a2))
  | <{~ b1}>      => negb (beval st b1)
  | <{b1 && b2}>  => andb (beval st b1) (beval st b2)
  end.

(** Мы можем использовать нашу нотацию для тотальных словарей и для состояний
    тоже -- т.е., записать пустое состояние как [(__ !-> 0)]. *)

Definition empty_st := (__ !-> 0).

(** Также, мы можем добавить нотацию для "состояния-синглетона", содержащего
    запись о единственной определённой переменной. *)
Notation "x '!->' v" := (x !-> v ; empty_st) (at level 100, right associativity).

Example aexp1 :
    aeval (X !-> 5) <{ 3 + (X * 2) }>
  = 13.
Proof. reflexivity. Qed.
Example aexp2 :
    aeval (X !-> 5 ; Y !-> 4) <{ Z + (X * Y) }>
  = 20.
Proof. reflexivity. Qed.

Example bexp1 :
    beval (X !-> 5) <{ true && ~(X <= 4) }>
  = true.
Proof. reflexivity. Qed.

